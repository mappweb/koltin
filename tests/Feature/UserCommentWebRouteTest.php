<?php

namespace Tests\Feature;

// use Illuminate\Foundation\Testing\RefreshDatabase;
use App\Models\Comment;
use App\Models\User;
use Database\Factories\UserCommentFactory;
use Illuminate\Foundation\Testing\WithFaker;
use Tests\TestCase;

class UserCommentWebRouteTest extends TestCase
{
    use WithFaker;

    /**
     * @var User|\Illuminate\Database\Eloquent\Collection|\Illuminate\Database\Eloquent\Model
     */
    protected User $user;
    /**
     * @var Comment|\Illuminate\Database\Eloquent\Collection|\Illuminate\Database\Eloquent\Model
     */
    protected Comment $comment;

    /**
     * Setup the test environment.
     *
     * @return void
     */
    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub

        /**
         * @var User $user
         */
        $this->user = User::factory()->create();
        $this->comment = Comment::factory(
            app(UserCommentFactory::class)->definition()
        )->create();
    }

    /**
     * Get modal create
     *
     * @return void
     */
    public function test_get_modal_create()
    {
        $route = route('user-comments.create', ['user' => $this->user->id]);
        // Try to access the page
        $response = $this->actingAs($this->user)
            ->get($route);
        // Assert the expected response status
        $response->assertStatus(200);
    }

    /**
     * Get modal edit
     *
     * @return void
     */
    public function test_get_modal_edit()
    {
        $route = route('user-comments.edit', ['comment' => $this->comment->id]);
        // Try to access the page
        $response = $this->actingAs($this->user)
            ->get($route);
        // Assert the expected response status
        $response->assertStatus(200);
    }

    /**
     * Store a newly created comment in storage
     *
     * @return void
     */
    public function test_user_comment_create()
    {
        $data = [
            'user_id' => $this->user->id,
            'content' => fake()->paragraph(),
        ];
        $route = route('user-comments.store');
        // Try to access the page
        $response = $this->actingAs($this->user)
            ->post($route, $data);
        // Assert the expected response status
        $response->assertStatus(200);
    }

    /**
     * Update the specified comment in storage.
     *
     * @return void
     */
    public function test_user_comment_update()
    {
        $data = [
            'user_id' => $this->user->id,
            'content' => fake()->paragraph(),
        ];
        $route = route('user-comments.update', ['comment' => $this->comment->id]);
        // Try to access the page
        $response = $this->actingAs($this->user)
            ->put($route, $data);
        // Assert the expected response status
        $response->assertStatus(200);
    }

}
